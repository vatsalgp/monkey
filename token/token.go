package token

type TokenType int

const (
	_ TokenType = iota

	// KEYWORDS
	LET
	FUNCTION
	RETURN

	IDENTIFIER

	// LITERALS
	INTEGER_LITERAL
	STRING_LITERAL
	FLOAT_LITERAL
	BOOLEAN_LITERAL

	// OPERATORS
	PLUS
	MINUS
	MULTIPLY
	DIVIDE
	ASSIGN

	// CHECKS
	IS_EQUAL
	IS_NOT_EQUAL
	IS_LESS_THAN
	IS_GREATER_THAN
	IS_LESS_THAN_OR_EQUAL
	IS_GREATER_THAN_OR_EQUAL

	// SYMBOLS
	COMMA
	SEMICOLON
	LEFT_PAREN
	RIGHT_PAREN
	LEFT_BRACE
	RIGHT_BRACE
)

type Token struct {
	Type	TokenType
	Literal	string
}
